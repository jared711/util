function startupbig()
%			startupbig.m
%
% This makes the axes and font sizes big so that it looks pretty in
% powerpoint.  Use this when you are generating plots for presentation
% purposes.
%
% S. Close 12/00


set(0,'defaultaxeslinewidth',1.5)
set(0,'defaultlinelinewidth',2)
set(0,'defaultaxesfontweight','bold')
set(0,'defaulttextfontweight','bold')
set(0,'defaultaxesfontsize',18)
set(0,'defaultaxesfontsize',18)
end